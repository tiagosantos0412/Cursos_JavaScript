/**
 * Operadores aritméticos
 * Adição -> 1 + 1
 * Subtração -> 2 - 4
 * Multiplicação -> 2 * 4
 * Divisão -> 2 / 4
 * Resto da Divisão -> 2 % 4
 * Exponenciação 2 ** 4
 */

/**
 * Operadores de comparação
 * Menor que 1 < 2
 * Maior que 2 > 4
 * Igual á 1 === 1
 * Diferente de 1 !== -1
 * Menor ou igual que 10 <= 10
 * Maior ou igual que 5 >= 4
 * Ou (idade < 18) || (idade > 25)
 * E (nome === "Erick") && (idade < 25)
 */
console.log("\n--- Operadores Aritméticos ---\n")
