/**
 * Em JavaScript, undefined é um tipo de dado que indica uma variável que foi declarada, 
 * mas não foi inicializada e não possui um valor atribuído.
 */
let x = undefined;
let y;

console.log(x);
console.log(y);

/**
 * null em JavaScript é um valor especial que indica a ausência intencional de qualquer valor ou objeto.
É diferente de undefined no sentido de que null é atribuído explicitamente para indicar a ausência de valor, 
enquanto undefined geralmente é o valor inicial padrão.
 */
let z = null
console.log(z)